void ft_putchar(char c);

void ft_print_comb(void)
{
	char v[3];

	v[0] = '0';
	while ((v[0] < '8') && (v[1] = v[0] + 1))
	{
		while (v[1] < '9')
		{
			v[2] = v[1] + 1;
			while (v[2] <= '9')
			{
				ft_putchar(v[0]);
				ft_putchar(v[1]);
				ft_putchar(v[2]);
				if (v[0] != '7')
				{
					ft_putchar(',');
					ft_putchar(' ');
				}
				v[2]++;
			}
			v[1]++;
		}
		v[0]++;
	}
	ft_putchar('\n');
}
