void ft_putchar(char c);

void ft_print_comb2(void)
{
	int v[2];

	v[0] = 0;
	while (v[0] < 99)
	{
		v[1] = v[0] + 1;
		while (v[1] < 100)
		{
			ft_putchar(v[0] / 10 + 48);
			ft_putchar(v[0] % 10 + 48);
			ft_putchar(' ');
			ft_putchar(v[1] / 10 + 48);
			ft_putchar(v[1] % 10 + 48);
			if ((v[0] < 98) || (v[1] < 99))
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
			v[1]++;
		}
		v[0]++;
	}
	ft_putchar('\n');
}
