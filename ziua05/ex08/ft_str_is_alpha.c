int		ft_str_is_alpha(char *str)
{
	int i;
	int ok;

	i = 0;
	ok = 0;
	if (*(str + i) == '\0')
		return (1);
	while (*(str + i) != '\0')
	{
		if ((str[i] > 64 && str[i] < 91) || (str[i] > 96 && str[i] < 123))
			ok = 1;
		else
			ok = 0;
		if (ok == 0)
			return (0);
		i++;
	}
	return (1);
}
